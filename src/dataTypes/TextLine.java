package dataTypes;

public class TextLine {
	private float fromY;
	private float toY;
	private float previousX;
	
	
	
	public float getPreviousX() {
		return previousX;
	}
	public void setPreviousX(float previousX) {
		this.previousX = previousX;
	}
	public float getFromY() {
		return fromY;
	}
	public void setFromY(float fromY) {
		this.fromY = fromY;
	}
	public float getToY() {
		return toY;
	}
	public void setToY(float toY) {
		this.toY = toY;
	}	
	
}
